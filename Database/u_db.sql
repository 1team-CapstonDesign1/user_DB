CREATE TABLE u_db (
	u_num INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	u_name VARCHAR(80) NOT NULL,
	u_age INT NOT NULL,
	u_gender INT NOT NULL,
	u_job VARCHAR(80) NOT NULL,
    u_bathing INT NOT NULL,
    u_water INT NOT NULL,
    u_temperature INT NOT NULL,
    u_time INT NOT NULL,
    u_last TINYINT NOT NULL
);
